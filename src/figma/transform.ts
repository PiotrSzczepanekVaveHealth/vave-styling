import {
  isFigmaColorValue,
  isFigmaVariableAlias,
  type FigmaColorValue,
  type FigmaVariable,
  type FigmaVariableCollection,
  type FigmaVariableValue,
  type FigmaVariablesPayload,
} from './types';

export type ThemeLeafValue = string | number | boolean | null;
export type ThemeValue = ThemeLeafValue | { readonly [key: string]: ThemeValue };
export type ThemeObject = Record<string, ThemeValue>;
export type ThemesObject = Record<string, ThemeObject>;
export type BreakpointsObject = Record<string, number>;

export interface TransformFigmaVariablesResult {
  themes: ThemesObject;
  breakpoints: BreakpointsObject;
  diagnostics: string[];
}

interface TransformContext {
  collectionsById: Map<string, FigmaVariableCollection>;
  variablesById: Map<string, FigmaVariable>;
  diagnostics: string[];
}

const COLLECTION_NAMESPACE_ALIASES: Record<string, string> = {
  color: 'colors',
  colors: 'colors',
  colour: 'colors',
  colours: 'colors',
  spacing: 'spacing',
  space: 'spacing',
  radius: 'radius',
  radii: 'radius',
  typography: 'typography',
  font: 'typography',
  fonts: 'typography',
  opacity: 'opacity',
  zindex: 'zIndex',
  zindices: 'zIndex',
  breakpoint: 'breakpoints',
  breakpoints: 'breakpoints',
};

const RESERVED_THEME_NAMES: Record<string, string> = {
  light: 'light',
  jasny: 'light',
  dark: 'dark',
  ciemny: 'dark',
};

export const transformFigmaVariables = (payload: FigmaVariablesPayload): TransformFigmaVariablesResult => {
  const collections = Object.values(payload.variableCollections);
  const variables = Object.values(payload.variables).filter((variable) => !variable.hiddenFromPublishing);
  const context: TransformContext = {
    collectionsById: new Map(collections.map((collection) => [collection.id, collection])),
    variablesById: new Map(variables.map((variable) => [variable.id, variable])),
    diagnostics: [],
  };

  const themeNames = collectThemeNames(collections);
  const themes = Object.fromEntries(themeNames.map((themeName) => [themeName, {} as ThemeObject]));
  const breakpoints: BreakpointsObject = {};

  for (const variable of variables) {
    const collection = context.collectionsById.get(variable.variableCollectionId);

    if (!collection || collection.hiddenFromPublishing) {
      continue;
    }

    const namespace = normalizeCollectionName(collection.name);
    const path = splitTokenPath(variable.name);

    if (path.length === 0) {
      context.diagnostics.push(`Skipped variable with empty path: ${variable.id}`);
      continue;
    }

    if (namespace === 'breakpoints') {
      const value = resolveVariableForTheme(variable, themeNames[0] ?? 'light', context);
      const breakpointName = path.at(-1);

      if (typeof value === 'number' && breakpointName) {
        breakpoints[breakpointName] = value;
      } else {
        context.diagnostics.push(`Skipped non-numeric breakpoint "${variable.name}".`);
      }

      continue;
    }

    for (const themeName of themeNames) {
      const value = resolveVariableForTheme(variable, themeName, context);

      if (value === undefined) {
        continue;
      }

      const theme = themes[themeName];

      if (!theme) {
        continue;
      }

      const convertedValue = convertVariableValue(value);

      if (convertedValue === undefined) {
        context.diagnostics.push(`Skipped unsupported value for "${variable.name}" in theme "${themeName}".`);
        continue;
      }

      const namespaceObject = ensureObject(theme, namespace);
      setDeepValue(namespaceObject, path, convertedValue);
    }
  }

  if (Object.keys(breakpoints).length === 0) {
    breakpoints.xs = 0;
    context.diagnostics.push('No breakpoint variables were found; generated a default xs: 0 breakpoint.');
  }

  return {
    themes,
    breakpoints,
    diagnostics: context.diagnostics,
  };
};

export const createGeneratedThemesSource = ({ themes, breakpoints, diagnostics }: TransformFigmaVariablesResult): string => {
  const header = [
    '/* eslint-disable */',
    '// This file is generated by scripts/generate-tokens.ts.',
    '// Do not edit it manually.',
    '',
  ];

  const diagnosticsComment =
    diagnostics.length > 0
      ? [
          '/**',
          ' * Generator diagnostics:',
          ...diagnostics.map((diagnostic) => ` * - ${diagnostic}`),
          ' */',
          '',
        ]
      : [];

  return `${[...header, ...diagnosticsComment].join('\n')}export const breakpoints = ${stringifyAsConst(
    breakpoints,
  )};\n\nexport const themes = ${stringifyAsConst(
    themes,
  )};\n\nexport type VaveBreakpoints = typeof breakpoints;\nexport type VaveThemeName = keyof typeof themes;\nexport type VaveTheme = (typeof themes)[VaveThemeName];\n`;
};


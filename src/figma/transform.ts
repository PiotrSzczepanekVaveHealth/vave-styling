const collectThemeNames = (collections: FigmaVariableCollection[]): string[] => {
  const names = new Set<string>();

  for (const collection of collections) {
    for (const mode of collection.modes) {
      names.add(normalizeThemeName(mode.name));
    }
  }

  const hasLight = names.has('light');
  const hasDark = names.has('dark');

  if (hasLight && hasDark) {
    return ['light', 'dark'];
  }

  return names.size > 0 ? [...names].sort() : ['light'];
};

const resolveVariableForTheme = (
  variable: FigmaVariable,
  themeName: string,
  context: TransformContext,
  seen = new Set<string>(),
): FigmaVariableValue | undefined => {
  const cycleKey = `${variable.id}:${themeName}`;

  if (seen.has(cycleKey)) {
    context.diagnostics.push(`Detected alias cycle while resolving "${variable.name}" for theme "${themeName}".`);
    return undefined;
  }

  seen.add(cycleKey);

  const collection = context.collectionsById.get(variable.variableCollectionId);

  if (!collection) {
    context.diagnostics.push(`Missing collection for variable "${variable.name}".`);
    return undefined;
  }

  const mode = findModeForTheme(collection, themeName);
  const rawValue = variable.valuesByMode[mode.modeId] ?? variable.valuesByMode[collection.defaultModeId];

  if (rawValue === undefined) {
    context.diagnostics.push(`Missing value for "${variable.name}" in theme "${themeName}".`);
    return undefined;
  }

  if (!isFigmaVariableAlias(rawValue)) {
    return rawValue;
  }

  const target = context.variablesById.get(rawValue.id);

  if (!target) {
    context.diagnostics.push(`Missing alias target "${rawValue.id}" for variable "${variable.name}".`);
    return undefined;
  }

  return resolveVariableForTheme(target, themeName, context, seen);
};

const findModeForTheme = (collection: FigmaVariableCollection, themeName: string) => {
  const exactMode = collection.modes.find((mode) => normalizeThemeName(mode.name) === themeName);
  const defaultMode = collection.modes.find((mode) => mode.modeId === collection.defaultModeId);

  return exactMode ?? defaultMode ?? collection.modes[0]!;
};

const convertVariableValue = (value: FigmaVariableValue): ThemeLeafValue | undefined => {
  if (isFigmaColorValue(value)) {
    return figmaColorToCss(value);
  }

  if (typeof value === 'string' || typeof value === 'number' || typeof value === 'boolean' || value === null) {
    return value;
  }

  return undefined;
};

export const figmaColorToCss = ({ r, g, b, a }: FigmaColorValue): string => {
  const red = colorChannelToHex(r);
  const green = colorChannelToHex(g);
  const blue = colorChannelToHex(b);
  const alpha = clamp(a, 0, 1);

  if (alpha >= 1) {
    return `#${red}${green}${blue}`;
  }

  const rgbaAlpha = Number(alpha.toFixed(3));

  return `rgba(${colorChannelToInt(r)}, ${colorChannelToInt(g)}, ${colorChannelToInt(b)}, ${rgbaAlpha})`;
};

const colorChannelToInt = (value: number): number => Math.round(clamp(value, 0, 1) * 255);

const colorChannelToHex = (value: number): string => colorChannelToInt(value).toString(16).padStart(2, '0');

const clamp = (value: number, min: number, max: number): number => Math.min(Math.max(value, min), max);

const normalizeThemeName = (name: string): string => {
  const normalized = toCamelCase(name);

  return RESERVED_THEME_NAMES[normalized.toLowerCase()] ?? normalized;
};

const normalizeCollectionName = (name: string): string => {
  const normalized = toCamelCase(name);

  return COLLECTION_NAMESPACE_ALIASES[normalized.toLowerCase()] ?? normalized;
};

const splitTokenPath = (name: string): string[] =>
  name
    .split(/[/.]/)
    .map((segment) => toCamelCase(segment.trim()))
    .filter(Boolean);

const toCamelCase = (value: string): string => {
  const words = value
    .replace(/['"]/g, '')
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean);

  if (words.length === 0) {
    return '';
  }

  const firstWord = words[0];

  if (!firstWord) {
    return '';
  }

  const restWords = words.slice(1);
  const first = startsWithNumber(firstWord) ? `_${firstWord}` : firstWord.toLowerCase();

  return [
    first,
    ...restWords.map((word) => `${word.charAt(0).toUpperCase()}${word.slice(1).toLowerCase()}`),
  ].join('');
};

const startsWithNumber = (value: string): boolean => /^\d/.test(value);

const ensureObject = (target: ThemeObject, key: string): ThemeObject => {
  const current = target[key];

  if (current && typeof current === 'object') {
    return current as ThemeObject;
  }

  const next: ThemeObject = {};
  target[key] = next;

  return next;
};

const setDeepValue = (target: Record<string, unknown>, path: string[], value: unknown): void => {
  let current = target;

  for (const segment of path.slice(0, -1)) {
    const child = current[segment];

    if (child && typeof child === 'object' && !Array.isArray(child)) {
      current = child as Record<string, unknown>;
      continue;
    }

    const next: Record<string, unknown> = {};
    current[segment] = next;
    current = next;
  }

  const leaf = path.at(-1);

  if (leaf) {
    current[leaf] = value;
  }
};

const stringifyAsConst = (value: unknown): string => `${JSON.stringify(value, null, 2)} as const`;
